% Add bsnr source, examples, and tests directories to the path.

thisDir   = fileparts(mfilename('fullpath'));
sourceDir = fileparts(thisDir);
testsDir  = fullfile(sourceDir, 'tests');
addpath(sourceDir, '-begin');
addpath(thisDir,   '-begin');
addpath(testsDir);

% Add external dependencies if present
for dep = {'longTermRecorders','annotatedLibrary','bsmTools','soundFolder'}
    candidate = fullfile('C:\analysis', dep{1});
    if exist(candidate,'dir'), addpath(candidate); end
